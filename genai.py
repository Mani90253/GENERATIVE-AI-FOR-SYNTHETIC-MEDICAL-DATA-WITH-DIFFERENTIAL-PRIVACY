def add_noise_to_image(image_tensor, noise_level=0.2):
    noise = torch.randn_like(image_tensor) * noise_level
    noisy_image = image_tensor + noise
    noisy_image = torch.clamp(noisy_image, -1, 1)
    return noisy_image

